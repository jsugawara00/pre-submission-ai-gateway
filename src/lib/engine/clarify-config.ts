/**
 * 確認チャット（聞き返し）の運用設定。
 * 依存を持たない定数・純関数のみ（サーバー／クライアント両方から安全に import できる）。
 */

/**
 * 再確認（AIが回答を受理できず突き返す）の上限回数。
 * これを超える回答はこの場では受け付けず、照合レポートの確認＋最初からのやり直しを促す。
 * 回数を変えたいときはこの値だけ変更すればよい。
 * 上限の強制はサーバー側（api/clarifications）で行う（堅牢性のためクライアントに依存しない）。
 */
export const MAX_RECONFIRM = 2;

/** 上限到達時にユーザーへ表示する案内文。 */
export function buildReconfirmLimitMessage(): string {
  return `再確認の上限（${MAX_RECONFIRM}回）に達しました。これ以上はこの場で確認できません。精査できなかった箇所は、下の照合レポートの内容でご確認ください。正しく照合し直したい場合は、レポートを確認のうえ、最初からやり直してください。`;
}
